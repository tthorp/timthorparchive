    <h2>My Boss wants me to update the website</h2>
    <p>by: Tim Thorp</p>
    <p> The website needs to be kept up to date and the boss can't do it. This is a fundamental need.They don't want to hire a full time web specialist - there may not be enough work, plus those folks are expensive. Who can do it? The office administrator, of course. Truth is that with some training and a reasonable scope-of-duty, an office administrator can update the website. </p>
    <h3>Boundaries</h3>
    Starting off with a reasonable set of boundaries will allow an office administrator to feel comfortable with their duties and their learning needs. It also sets a great framework for me to write this book:) Trouble is, without a web shop to set those priorities, it may be difficult for the boss or the employee to know what constitutes a reasonable work request. So let's start by establishing what work is done in maintaining website and separate admin staff duties from things that executives, consultants and experts need to do.

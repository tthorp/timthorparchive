<h2>1979 Honda Civic Hatchback</h2>
<p>Bought it when I was 14 years old for $100. It had been totalled in an accident, but I pried the fender off the back tire with a cro-bar and replaced the rear light that was broken. Stick shift with an 1,169 cc engine. I once drove it 110 miles per hour on the rte. 4 hill that runs through East Greenwich between the milk farm and the high school. Also, took it for "air" several times on upper Middle Road. Kept it until I was 17.</p>

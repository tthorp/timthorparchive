<h2>1984 Audi 4000s</h2>
<p>Nice car for a teenager who bought with his own hard-earned money @ $3.25/hr. Stick shift, really expensive to run due to fuel injection issues...had it serviced by Hans Imports in North Kingstown. Kept this until I was 19.</p>
<img src="/img/vehicles/1984_audi.jpg" />

<h2>Honda Spree</h2>
<p>I got this for around $100. Fun to ride around on but probably looked silly. When it was about to run out of gas, it would speed up to ~40mph. Worked great until I tried to jump-start it but reversed the polarity and fried the electric system.</p>
<img src="/img/vehicles/spree.jpg" />

<h2>1979 Fiat Spyder</h2>
<p>I managed to own or borrow this for a summer. Fun to have a convertible except for that one time when I floored it to ascend a hill on rte 1 and the gas pedal got stuck. It was a hairy situation as I approached a construction site at 80mph and couldn't unstick the gas. Eventually, I shut off the ignition, but not early enough to escape the police officer that was pretty upset with my driving.</p>
<img src="/img/vehicles/1979_fiat.jpg" />

